#include "main.h"

#include "mpi/MPIWrapper.h"

int main(int argc, char** argv) {
    MPIWrapper::init(argc, argv);

    ::benchmark::Initialize(&argc, argv);
    if (::benchmark::ReportUnrecognizedArguments(argc, argv))
        return 1;
    ::benchmark::RunSpecifiedBenchmarks();
    ::benchmark::Shutdown();
    return 0;
}
